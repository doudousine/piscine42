#include <stdio.h>

int		ft_iterative_factorial(int nb)
{
	int fct;

	fct = 1;
	if (nb <= 0 || nb > 12)
		return (0);
	while (nb > 0)
	{
		fct = fct * nb;
		nb--;
	}
	return (fct);
}
