int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				result;

	i = 0;
	while (s1[i] != '\0' && s1[i] == s2[i] && i < n)
	{
		result = s1[i] - s2[i];
		i++;
	}
	return (s1[i] - s2[i]);
}
